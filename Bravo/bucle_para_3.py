# FA=Facultad
# C=de Ciencias
# F=Fisicas
# Y=y
# M=Matematicas
MSG="FACFYM"
for letra in MSG:
    if letra == "FA":
        print("Facultad")
    if letra == "C":
        print("de Ciencias")
    if letra == "F":
        print("Fisicas")
    if letra == "Y":
        print("y")
    if letra == "M":
        print("Matematicas")
