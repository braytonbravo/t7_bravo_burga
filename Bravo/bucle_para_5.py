#DECODIFICAR MENSAJE ENCRIPTADO
# B=Bueno
# R=Respetuoso
# A=Amable
# V=Valiente
# 0=Optimista
MSG="BRAVO"
for letra in MSG:
    if letra == "B":
        print("Bueno")
    if letra == "R":
        print("Respetuoso")
    if letra == "A":
        print("Amable")
    if letra == "V":
        print("Valiente")
    if letra == "O":
        print("Optimista")
