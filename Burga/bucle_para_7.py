# Decodificar mensaje encriptado
# a = ¿como estas?
# b = que bien
# s = cuidate
# w = saludos
msg="absw"

for letra in msg:
    if letra == "a":
        print("¿como estas?")
    if letra == "b":
        print("que bien")
    if letra == "s":
        print("cuidate")
    if letra == "w":
        print("saludos")
#fin_iterador

print("Fin del bucle")
